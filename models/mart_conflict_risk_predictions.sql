WITH latest_country_data AS (
    -- Step 1: Grab only the most recent year of data for each country
    SELECT 
        country,
        year, 
        total_fatalities,
        total_events,
        violence_types_present,
        population,
        gdp,
        gdp_per_capita,
        fatalities_per_100k_people
    FROM {{ ref('mart_country_conflict_features') }}
    QUALIFY ROW_NUMBER() OVER (PARTITION BY country ORDER BY year DESC) = 1
),

predictions AS (
    -- Step 2: Feed that recent data into your upgraded v2 BigQuery ML model
    SELECT 
        country,
        year,
        total_events,
        population,
        -- The new 2-year probability array generated by BigQuery ML
        predicted_target_conflict_next_2_years_probs 
    FROM ML.PREDICT(
        MODEL `global-conflicts-500009.raw_conflict_data.predict_conflict_risk`, 
        (
            SELECT * FROM latest_country_data
        )
    )
)

-- Step 3: Clean up the output for your final Looker Studio dashboard
SELECT 
    country,
    year AS last_data_year,
    total_events AS recent_conflict_events,
    
    -- BQML outputs an array of probabilities. We extract the probability of '1' (Conflict)
    ROUND(prob.prob * 100, 2) AS conflict_probability_percent,
    
    -- Categorize the risk using stricter v2 thresholds (The "Australia Fix")
    CASE 
        WHEN prob.prob > 0.95 THEN 'High Risk'
        WHEN prob.prob > 0.83 THEN 'Medium Risk'
        WHEN prob.prob > 0.80 THEN 'Low Risk'
        ELSE 'Very Low Risk'
    END AS risk_category

FROM predictions, UNNEST(predicted_target_conflict_next_2_years_probs) AS prob
WHERE prob.label = 1 
ORDER BY conflict_probability_percent DESC